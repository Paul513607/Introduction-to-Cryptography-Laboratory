    public static String xorBitStrings(String bitString1, String bitString2) throws IllegalArgumentException {
        if (bitString1.length() != bitString2.length())
            throw new IllegalArgumentException("BitStrings must have the same size in order to XOR them!");

        StringBuilder result = new StringBuilder();
        for (int index = 0; index < bitString1.length(); ++index) {
            if (bitString1.charAt(index) != '0' && bitString1.charAt(index) != '1' || bitString2.charAt(index) != '0' && bitString2.charAt(index) != '1')
                new IllegalArgumentException("Unexpected character in one of the bitStrings!");

            if (bitString1.charAt(index) == bitString2.charAt(index))
                result.append('0');
            else
                result.append('1');
        }

        return result.toString();
    }
